    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String varLog=txtLogin.getText();
        String varSenha=txtSenha.getText();
        //comparar com o BD
        
        ListaProcesso prt01=new ListaProcesso();
        
        try { 
            //if(true){
            if(use.login(varLog, varSenha)){
                cartao01.removeAll();
                cartao01.add(prt01);
                txtLogin.setText("");
                txtSenha.setText("");
                prt01.setPrincipal(this);


                use.carregaFormulario(prt01);
                prt01.setUse(use);


                cartao01.repaint();
                cartao01.revalidate();

            }else{
                JOptionPane.showMessageDialog(null, "Login ou senha invÃ¡lido", "Erro ao Logar!", JOptionPane.ERROR_MESSAGE, null);
            }
        } catch (CloneNotSupportedException ex) {
                Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnLoginActionPerformed

