    private void txtSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSenhaKeyPressed
        if(txtSenha.getPassword().length>35){
           // JOptionPane.showMessageDialog(null,"Numero excessimo de Caracteres, limite de 35 caracteres","Erro na Senha",JOptionPane.ERROR_MESSAGE,null);
            char[] senha=txtSenha.getPassword();
            String novaSenha="";
            int i;
            for(i=0;i<36;i++){
               novaSenha+=senha[i];
            }
            txtSenha.setText(novaSenha);
        }
    }//GEN-LAST:event_txtSenhaKeyPressed

    private void txtLoginKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtLoginKeyPressed
        if(txtLogin.getText().getBytes().length>50){
           // JOptionPane.showMessageDialog(null,"Numero excessimo de Caracteres, limite de 35 caracteres","Erro na Senha",JOptionPane.ERROR_MESSAGE,null);
            char[] nome=txtLogin.getText().toCharArray();
            String novoNome="";
            int i;
            for(i=0;i<51;i++){
               novoNome+=nome[i];
            }
            txtLogin.setText(novoNome);
        }
    }//GEN-LAST:event_txtLoginKeyPressed
    public void Logoff() {                                              
        if(use.logoff()){
            cartao01.removeAll();
            cartao01.add(tela00);
            cartao01.repaint();
            cartao01.revalidate();
        }else{
            JOptionPane.showMessageDialog(null, "Falha no Logoff", "Erro ao Deslogar!", JOptionPane.ERROR_MESSAGE, null);
        }
    } 
